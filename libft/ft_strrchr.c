#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	test;

	test = 0;
	if (*s == '\0' && c == '\0')
		test = 1;
	while (*s != '\0')
	{
		if (*s == c)
			test = 1;
		s++;
		if (*s == '\0' && c == '\0')
			test = 1;
	}
	if (test == 0)
		return (NULL);
	while (*s != c)
	{
		s--;
	}
	return ((char *)s);
}
