#include "libft.h"

int		ft_isstrblank(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (ft_strchr("\t ", str[i]) == 0)
			return (0);
		i++;
	}
	return (1);
}
