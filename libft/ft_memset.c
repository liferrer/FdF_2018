#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int		i;
	char	*cb;

	cb = (char *)b;
	i = 0;
	while ((size_t)i < len)
	{
		cb[i] = c;
		i++;
	}
	return (b);
}
